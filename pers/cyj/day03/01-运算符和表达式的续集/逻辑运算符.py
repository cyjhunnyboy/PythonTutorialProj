"""
逻辑与：and
逻辑与运算表达式：表达式1 and 表达式2
值：如果表达式1的值为真，表达式2的值为真，整个逻辑与运算表达式的值为真，否则为假
"""
num1 = 10
num2 = 20
if num1 - 10 and num2:
    print("***********")

"""
逻辑或：or
逻辑或运算表达式：表达式1 or 表达式2
值：如果表达式1和表达式2的值其中有一个为真正，整个逻辑或运算表达式的值为真，否则为假
"""
num3 = 0
num4 = 1
if num3 or num4:
    print("逻辑或表达式结果为真")

"""
逻辑非：not
逻辑非运算表达式：not 表达式
值：如果“表达式”的值为真，则整个逻辑非运算表达式的值为假，否则为真
"""
num5 = False
if not num5:
    print("逻辑非运算表达式结果为真")

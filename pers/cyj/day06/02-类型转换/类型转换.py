# list -> set
list1 = [1, 2, 3, 4, 5, 4, 3, 2, 1]
set1 = set(list1)
print(list1)
print(set1)

# tuple -> set
tuple1 = (1, 2, 3, 4, 3, 2)
set2 = set(tuple1)
print(tuple1)
print(set2)

# set -> list
set3 = {1, 2, 3}
list3 = list(set3)
print(set3)
print(list3)


# set -> tuple
set4 = {1, 2, 3, 4, 5}
tuple4 = tuple(set4)
print(set4)
print(tuple4)

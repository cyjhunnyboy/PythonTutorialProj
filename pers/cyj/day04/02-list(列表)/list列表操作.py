# 列表组合
list1 = [1, 2, 3]
list2 = [4, 5, 6]
list3 = list1 + list2
print(list1)
print(list2)
print(list3)

# 列表的重复
list4 = [1, 2, 3]
print(list4 * 3)

# 判断元素是否在列表中
list5 = [1, 2, 3, 4, 5, 6, 7, 8, 9]
print(3 in list5)
print(10 in list5)

# 列表截取
list6 = [1, 2, 3, 4, 5, 6, 7, 8, 9]
print(list6[2:6])
print(list6[3:])
print(list6[:5])
print(list6[:])

# 二维列表
list7 = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]
print(list7[1])
print(list7[1][1])
